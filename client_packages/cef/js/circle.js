var circle = new Vue({
    el: '.circle',
    data: {
        active: false,
        icons: [null,null,null,null,null,null,null,null],
        items: [],
        description: null,
        title: "title",
    },
    methods:{
        set: function(t,id){
            this.description = t
            this.title = t
            this.icons = circleData[t][id]
            this.items = [];
            for (let idx in this.icons) {
                this.items.push(circleDesc[this.icons[idx]])
            }
        },
        out: function(e){
            this.description = this.title
            //console.log('out')
        },
        btn: function(e){
            let id = e.target.id
            if(id == '50'){
                mp.trigger("circleCallback", -1);
                this.hide();
                return;
            }
            mp.trigger("circleCallback", Number(e.target.id));
            this.hide();
        },
        show: function(t,id){
            this.active=true
            this.set(t,id)
            setTimeout(()=>{move('.circle').set('width', '250px').set('height', '240px').set('opacity', 1).end()}, 50);
        },
        hide: function(){
            //move('.circle').set('width', '80px').set('height', '80px').set('opacity', 0).end(()=>{circle.active=false})
            circle.active = false;
        }
    }
})